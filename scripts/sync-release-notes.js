// scripts/sync-release-notes.js
// Copies RELEASE_NOTES.md into constants/release-notes.ts as a string export.
// Run automatically via postinstall.

const fs = require("fs");
const path = require("path");

const mdPath = path.join(__dirname, "..", "RELEASE_NOTES.md");
const outPath = path.join(__dirname, "..", "constants", "release-notes.ts");

const md = fs.readFileSync(mdPath, "utf8").trimEnd();

const content = `// Auto-generated by scripts/sync-release-notes.js — do not edit manually.
// Source: RELEASE_NOTES.md

export const RELEASE_NOTES_MD = \`${md.replace(/\\/g, "\\\\").replace(/`/g, "\\`")}\`;
`;

fs.writeFileSync(outPath, content);
console.log("Synced release notes to", outPath);
